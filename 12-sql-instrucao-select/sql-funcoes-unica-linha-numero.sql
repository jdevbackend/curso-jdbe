-- ROUND: FUNÇÃO PARA ARREDONDAMENTO DE VALORES
SELECT ROUND(195.293), ROUND(195.299, 2), ROUND(195.292, -1)
  FROM DUAL;

-- TRUNC: FUNÇÃO PARA TRUNCAR UM VALOR
SELECT TRUNC(195.293), TRUNC(195.299, 2), TRUNC(195.292, -1)
  FROM DUAL;

-- MOD: RETORNA O RESTO DA DIVISÃO ENTRE DOIS NÚMEROS. UTILIZADA TAMBÉM PARA 
-- IDENTIFICAR SE UM NÚMERO É PAR/ÍMPAR
SELECT MOD(3,2)
  FROM DUAL;