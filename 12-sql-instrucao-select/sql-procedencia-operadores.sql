-- PROCEDÊNCIA
-- MULTIPLICAÇÃO (*) -> DIVISÃO (/) -> SOMA (+) -> SUBTRAÇÃO (-)
SELECT C.ID_CURSO, 
       C.NM_TITULO, 
       C.VL_PRECO,
       C.VL_PRECO - 10 * 30, -- NESTE CASO, A MULTIPLICAÇÃO É EXECUTADO PRIMEIRO
       (C.VL_PRECO - 10) * 30-- AQUI FORÇAMOS A EXECUÇÃO DA SUBTRAÇÃO ANTES DA
                             -- MULTIPLICAÇÃO
  FROM CURSO C;

