3) OS CARACTERES PERMITIDOS SÃO: A-Z, a-z, 0-9, _, $ E #;
4) NOMES DE OBJETOS NÃO PODEM SE REPETIR, DEVEM SER ÚNICOS;
5) NÃO PODEMOS UTILIZAR PALAVRAS RESERVADAS DO ORACLE, COMO POR EXEMPLO: TABLE,
   CONSTRAINT, MODIFY E ETC.
   
